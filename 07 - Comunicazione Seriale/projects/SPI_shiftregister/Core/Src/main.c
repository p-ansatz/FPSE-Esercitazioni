#include "main.h"

/* Private variables ---------------------------------------------------------*/
SPI_HandleTypeDef hspi1;
UART_HandleTypeDef huart2;

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART2_UART_Init(void);
static void MX_SPI1_Init(void);

/* Define Colours- Shift Register Outputs
 * QA -> NO
 * QB -> RED
 * QC -> NO
 * QD -> GREEN
 * QE -> NO
 * QF -> BLU
 *
 * Q-H G F E D C B A
 * 0b0 0 0 0 0 0 0 0
 * */

#define RED 0b00000010
#define GRE 0b00001000
#define BLU 0b00100000
#define ORA 0b00001010
#define VIO 0b00100010
#define LBL 0b00101000
#define WHI 0b00101010

/* Output on Shift Register */
void ShiftReg_out(uint8_t *data, uint16_t size){
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_RESET);
	  HAL_SPI_Transmit(&hspi1, data, size, 100);
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_SET);
}

/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void){

	/* MCU Configuration--------------------------------------------------------*/
	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();
	/* Configure the system clock */
	SystemClock_Config();
	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART2_UART_Init();
	MX_SPI1_Init();

	uint8_t outputBuffer[1]; // Output Buffer for SPI
	char read_char[1];		 // Char from UART

	/* Infinite loop */
	while (1){
		HAL_UART_Receive(&huart2, (uint8_t*)read_char, 1, HAL_MAX_DELAY);
		switch(read_char[0]){
			case 'R':
				outputBuffer[0] = RED;
				break;
			case 'G':
				outputBuffer[0] = GRE;
				break;
			case 'B':
				outputBuffer[0] = BLU;
				break;
			case 'O':
				outputBuffer[0] = ORA;
				break;
			case 'L':
				outputBuffer[0] = LBL;
				break;
			case 'V':
				outputBuffer[0] = VIO;
				break;
			default:
				outputBuffer[0] = WHI;
			break;
		}
		ShiftReg_out(outputBuffer, 1);

	  HAL_Delay(500);
	}

}

