/* Private variables ---------------------------------------------------------*/
ADC_HandleTypeDef hadc1; // ADC
TIM_HandleTypeDef htim2; // TIMER2
/* ADC input variables */
uint16_t raw_in;
float temp_mv;
float temp;

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_ADC1_Init(void);
static void MX_TIM2_Init(void);

void LED_Init(void);


/* ADC ISR Handler*/
void ADC_IRQHandler(void) {
	HAL_ADC_IRQHandler(&hadc1);
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
	// Read the analog input value
	raw_in = HAL_ADC_GetValue(&hadc1);
	// Converts value in the 0V-3.3V range
	temp_mv = ((float)raw_in) / 4095 * 3300;
	// Convert mV to temperature
	temp = (1035 - temp_mv)/5.50f;

	// Led Toggle
	GPIOA->ODR ^= (0x1 << 5);
}

int main(void){
	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* Configure the system clock */
	SystemClock_Config();

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_ADC1_Init(); // ADC initialization
	MX_TIM2_Init(); // TIMER initialization

	LED_Init();

	/* Starting ADC/TIMER */
	HAL_TIM_Base_Start(&htim2); // Start Timer - no interrupt mode

	NVIC_SetPriority(ADC_IRQn, 0); //Interrupt configuration for ADC
	NVIC_EnableIRQ(ADC_IRQn);
	HAL_ADC_Start_IT(&hadc1); // Start ADC - interrupt

	/* Infinite loop */
	while (1){
		;
	}
}

void LED_Init(void){
	// Init led on PA_5
	RCC->AHB1ENR |= 0x01;
	GPIOA->MODER |= (0x01 << 10);
	GPIOA->OTYPER &= ~(0x01 << 5);
	GPIOA->PUPDR &= ~(0x03 << 10);
	GPIOA->PUPDR |= (0x01 << 5);

	GPIOA->ODR |= (0x01 << 5); // LED ON
}


