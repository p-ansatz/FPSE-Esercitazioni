void LED_Init(void){
	// Init led on PA_5
	RCC->AHB1ENR |= 0x01;
	GPIOA->MODER |= (0x01 << 10);
	GPIOA->OTYPER &= ~(0x01 << 5);
	GPIOA->PUPDR &= ~(0x03 << 10);
	GPIOA->PUPDR |= (0x01 << 5);

	GPIOA->ODR |= (0x01 << 5); // LED ON
}


